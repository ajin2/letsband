package board;

public class FileDataBean {
	private int num;
	private int f_id;
	private String ori;
	private String sys;
	private int f_size;
	
	public int getNum() {
		return num;
	}
	public int getF_id() {
		return f_id;
	}
	public String getOri() {
		return ori;
	}
	public String getSys() {
		return sys;
	}
	public int getF_size() {
		return f_size;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public void setOri(String ori) {
		this.ori = ori;
	}
	public void setSys(String sys) {
		this.sys = sys;
	}
	public void setF_size(int f_size) {
		this.f_size = f_size;
	}
}
